package Practical1;

public class Circle {

    public double getarea(double a){
        System.out.println("The Area of Circle is "+3.14*a*a);
        return(3.14*a*a);
    }
    public double getPerimeter(double b){
        System.out.println("The Perimeter of Circle is "+2*3.14*b);
        return(2*3.14*b);
    }
}
